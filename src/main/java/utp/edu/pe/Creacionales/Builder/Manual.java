package utp.edu.pe.Creacionales.Builder;

public class Manual {

    String doc;
    String Carac;

    public Manual() {
    }

    public String getDoc() {
        return doc;
    }

    public void setDoc(String documentacion) {
        this.doc = documentacion;
    }

    public String getCarac() {
        return Carac;
    }

    public void setCarac(String caracteristicas) {
        Carac = caracteristicas;
    }
}
